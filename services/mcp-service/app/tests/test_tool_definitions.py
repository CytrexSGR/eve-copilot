class TestGenerateToolName:
    """Tests for MCP tool name generation from OpenAPI endpoints."""

    def test_uses_clean_operation_id(self, make_endpoint):
        ep = make_endpoint(operation_id="search_corporation")
        assert generate_tool_name(ep) == "search_corporation"

    def test_strips_api_suffix_from_autogenerated_id(self, make_endpoint):
        ep = make_endpoint(operation_id="get_stats_api_market_stats_get")
        assert generate_tool_name(ep) == "get_stats"

    def test_fallback_to_path_when_no_operation_id(self, make_endpoint):
        ep = make_endpoint(operation_id="", path="/api/market/price/{type_id}", method="GET")
        name = generate_tool_name(ep)
        assert name == "get_market_price_type_id"

    def test_fallback_removes_api_prefix(self, make_endpoint):
        ep = make_endpoint(operation_id="", path="/api/war/losses", method="GET")
        assert generate_tool_name(ep) == "get_war_losses"

    def test_post_method_prefix(self, make_endpoint):
        ep = make_endpoint(operation_id="", path="/api/market/bulk", method="POST")
        assert generate_tool_name(ep) == "post_market_bulk"

    def test_delete_method_prefix(self, make_endpoint):
        ep = make_endpoint(operation_id="", path="/api/shopping/lists/1", method="DELETE")
        name = generate_tool_name(ep)
        assert name.startswith("delete_")

    @pytest.mark.parametrize("op_id,expected", [
        ("list_battles", "list_battles"),
        ("get_items_api_items_search_get", "get_items"),
        ("create_api_views_post", "create"),
    ])
    def test_operation_id_cleanup(self, make_endpoint, op_id, expected):
        ep = make_endpoint(operation_id=op_id)
        assert generate_tool_name(ep) == expected


