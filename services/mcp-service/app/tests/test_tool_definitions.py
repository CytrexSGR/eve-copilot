class TestOpenAPIParser:
    """Tests for OpenAPI spec parsing logic."""

    def test_parse_simple_spec(self):
        from app.openapi.parser import OpenAPIParser
        parser = OpenAPIParser()
        parser.specs["test_svc"] = {
            "paths": {
                "/api/test": {
                    "get": {
                        "operationId": "get_test",
                        "summary": "Test endpoint",
                        "description": "",
                        "parameters": [],
                        "responses": {"200": {}},
                        "tags": ["test"],
                    }
                }
            }
        }
        endpoints = parser.parse_endpoints("test_svc")
        assert len(endpoints) == 1
        assert endpoints[0].operation_id == "get_test"
        assert endpoints[0].method == "GET"

    def test_parse_filters_mcp_only(self):
        from app.openapi.parser import OpenAPIParser
        parser = OpenAPIParser()
        parser.specs["svc"] = {
            "paths": {
                "/api/test": {"get": {"operationId": "a", "summary": "", "description": "",
                                       "parameters": [], "responses": {}, "tags": []}},
                "/api/mcp/search": {"get": {"operationId": "b", "summary": "", "description": "",
                                             "parameters": [], "responses": {}, "tags": []}},
            }
        }
        all_eps = parser.parse_endpoints("svc", mcp_only=False)
        assert len(all_eps) == 2

        mcp_eps = parser.parse_endpoints("svc", mcp_only=True)
        assert len(mcp_eps) == 1
        assert mcp_eps[0].operation_id == "b"

    def test_parse_ignores_non_http_methods(self):
        from app.openapi.parser import OpenAPIParser
        parser = OpenAPIParser()
        parser.specs["svc"] = {
            "paths": {
                "/api/test": {
                    "get": {"operationId": "ok", "summary": "", "description": "",
                            "parameters": [], "responses": {}, "tags": []},
                    "parameters": [{"name": "shared"}],  # path-level params, not a method
                }
            }
        }
        endpoints = parser.parse_endpoints("svc")
        assert len(endpoints) == 1

    def test_parse_no_spec_returns_empty(self):
        from app.openapi.parser import OpenAPIParser
        parser = OpenAPIParser()
        assert parser.parse_endpoints("unknown_service") == []

    def test_parse_multiple_methods(self):
        from app.openapi.parser import OpenAPIParser
        parser = OpenAPIParser()
        parser.specs["svc"] = {
            "paths": {
                "/api/items": {
                    "get": {"operationId": "list", "summary": "", "description": "",
                            "parameters": [], "responses": {}, "tags": []},
                    "post": {"operationId": "create", "summary": "", "description": "",
                             "parameters": [], "responses": {}, "tags": []},
                }
            }
        }
        endpoints = parser.parse_endpoints("svc")
        methods = {ep.method for ep in endpoints}
        assert methods == {"GET", "POST"}


