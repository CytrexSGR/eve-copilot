class TestBuildInputSchema:
    """Tests for MCP input schema generation from OpenAPI parameters."""

    def test_path_parameter(self, make_endpoint):
        ep = make_endpoint(parameters=[{
            "name": "type_id",
            "in": "path",
            "required": True,
            "schema": {"type": "integer"},
        }])
        schema = build_input_schema(ep)
        assert schema["type"] == "object"
        assert "type_id" in schema["properties"]
        assert schema["properties"]["type_id"]["type"] == "integer"
        assert "type_id" in schema["required"]

    def test_query_parameter_with_default(self, make_endpoint):
        ep = make_endpoint(parameters=[{
            "name": "region_id",
            "in": "query",
            "required": False,
            "schema": {"type": "integer", "default": 10000002},
            "description": "The Forge region",
        }])
        schema = build_input_schema(ep)
        props = schema["properties"]
        assert props["region_id"]["default"] == 10000002
        assert props["region_id"]["description"] == "The Forge region"
        assert "region_id" not in schema["required"]

    def test_request_body_properties(self, make_endpoint):
        ep = make_endpoint(
            method="POST",
            request_body={
                "content": {
                    "application/json": {
                        "schema": {
                            "type": "object",
                            "properties": {
                                "name": {"type": "string", "description": "List name"},
                                "quantity": {"type": "integer"},
                            },
                            "required": ["name"],
                        }
                    }
                }
            },
        )
        schema = build_input_schema(ep)
        assert "name" in schema["properties"]
        assert "quantity" in schema["properties"]
        assert "name" in schema["required"]

    def test_no_parameters_returns_empty_schema(self, make_endpoint):
        ep = make_endpoint(parameters=[], request_body=None)
        schema = build_input_schema(ep)
        assert schema == {"type": "object", "properties": {}, "required": []}

    def test_required_deduplication(self, make_endpoint):
        """If same param is in both path params and body required, no duplicates."""
        ep = make_endpoint(
            parameters=[{
                "name": "id",
                "in": "path",
                "required": True,
                "schema": {"type": "integer"},
            }],
            request_body={
                "content": {
                    "application/json": {
                        "schema": {
                            "properties": {"id": {"type": "integer"}},
                            "required": ["id"],
                        }
                    }
                }
            },
        )
        schema = build_input_schema(ep)
        assert schema["required"].count("id") == 1

    def test_param_without_name_skipped(self, make_endpoint):
        ep = make_endpoint(parameters=[{"in": "query", "schema": {"type": "string"}}])
        schema = build_input_schema(ep)
        assert schema["properties"] == {}


